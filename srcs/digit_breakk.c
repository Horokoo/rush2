void	digit_break(char *c)
{
	int i;
	int ic;
	int j;
	int k;
	int v;

	i = 0;
	j = 0;
	k = 0;
	ic = 0;
	while (c[i])
	{
		i++;
	}
	while (j < (i - 1) && (ic < i - 1))
	{
		k = j;
		v = c[ic] - 48;
		while (k++ < (i - 1))
			v = v * 10;
		printf("\n%d", v);
		ic++;
		j++;
	}
	printf("\n%c", c[ic]);
}
